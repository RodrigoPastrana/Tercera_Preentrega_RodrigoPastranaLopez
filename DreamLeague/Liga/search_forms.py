from django import forms
from .models import División, Equipo, Jugador


class DivisionSearchForm(forms.Form):
    query = forms.CharField(label="Buscar División", max_length=255)


class EquipoSearchForm(forms.Form):
    query = forms.CharField(label="Buscar Equipo", max_length=255)


class JugadorSearchForm(forms.Form):
    query = forms.CharField(label="Buscar Jugador", max_length=255)
